void	ft_rev_int_tab(int *tab, int size)
{
	int	helper;
	int	s;
	int	e;

	s = 0;
	e = size - 1;
	while (e >= s + 1)
	{
		helper = *(tab + s);
		*(tab + s) = *(tab + e);
		*(tab + e) = helper;
		s++;
		e--;
	}
}
