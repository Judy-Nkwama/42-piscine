int	ft_str_is_printable(char *str)
{
	int	i;
	int	c;
	int	ret_val;

	ret_val = 1;
	i = 0;
	while (*(str + i) != '\0')
	{
		c = (int)(*(str + i));
		if (!((c >= 32) && (c <= 126)))
		{
			ret_val = 0;
			break ;
		}
		i++;
	}
	return (ret_val);
}
